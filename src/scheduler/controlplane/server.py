@app.get("/auth/google/callback")
def auth_google_connect_callback(
    code: str | None = None, state: str | None = None, error: str | None = None,
    scope: str | None = None,
):
    """Handle Google OAuth callback for the Connect flow."""
    if error:
        return RedirectResponse(f"{config.web_app_url}?error={error}")

    if not code or not state:
        return RedirectResponse(f"{config.web_app_url}?error=missing_params")

    # Validate state
    issued_at = _google_connect_states.pop(state, None)
    if not issued_at or (time.time() - issued_at > 600):
        return RedirectResponse(f"{config.web_app_url}?error=invalid_state")

    # Check that user granted all required scopes
    granted_scopes = set(scope.split()) if scope else set()
    missing_scopes = _REQUIRED_GOOGLE_SCOPES - granted_scopes
    if missing_scopes:
        logger.warning("google_connect: missing scopes %s, re-prompting", missing_scopes)
        # Re-store state so the next callback can use it
        _google_connect_states[state] = time.time()
        params = {
            "client_id": config.google_client_id,
            "redirect_uri": f"{config.google_web_redirect_uri}/auth/google/callback",
            "response_type": "code",
            "scope": " ".join(SCOPES),
            "access_type": "offline",
            "prompt": "consent",
            "state": state,
            "include_granted_scopes": "true",
        }
        return RedirectResponse(
            f"{config.web_app_url}/permissions-required?retry_url={urllib.parse.quote(f'https://accounts.google.com/o/oauth2/v2/auth?{urllib.parse.urlencode(params)}')}"
        )

    # Extract user_id and auth_token from state
    parts = state.split("|")
    state_dict = {}
    for part in parts[1:]:
        if "=" in part:
            k, v = part.split("=", 1)
            state_dict[k] = v
    user_id = state_dict.get("user_id")
    auth_token = state_dict.get("auth_token")

    if not user_id:
        return RedirectResponse(f"{config.web_app_url}?error=invalid_state")

    import httpx

    # Exchange authorization code for Google tokens
    token_response = httpx.post(
        "https://oauth2.googleapis.com/token",
        data={
            "code": code,
            "client_id": config.google_client_id,
            "client_secret": config.google_client_secret,
            "redirect_uri": f"{config.google_web_redirect_uri}/auth/google/callback",
            "grant_type": "authorization_code",
        },
    )

    if token_response.status_code != 200:
        logger.error("Google token exchange failed: %s", token_response.text)
        return RedirectResponse(f"{config.web_app_url}?error=token_exchange_failed")

    tokens = token_response.json()
    google_access_token = tokens["access_token"]
    google_refresh_token = tokens.get("refresh_token")

    if not google_refresh_token:
        return RedirectResponse(f"{config.web_app_url}?error=no_refresh_token")

    from scheduler.db import update_google_tokens

    expires_in = tokens.get("expires_in")
    expires_at = None
    if expires_in:
        from datetime import timedelta
        expires_at = datetime.now() + timedelta(seconds=expires_in)

    update_google_tokens(
        user_id=user_id,
        google_refresh_token=google_refresh_token,
        google_access_token=google_access_token,
        access_token_expires_at=expires_at,
    )

    # Fetch and store display name + Google email from Google profile
    try:
        import httpx as _httpx
        _userinfo = _httpx.get(
            "https://www.googleapis.com/oauth2/v2/userinfo",
            headers={"Authorization": f"Bearer {google_access_token}"},
        )
        if _userinfo.status_code == 200:
            _info = _userinfo.json()
            _name = _info.get("name")
            _google_email = _info.get("email")
            if _name:
                from scheduler.db import update_display_name
                update_display_name(user_id, _name)
            if _google_email:
                from scheduler.db import update_google_email
                update_google_email(user_id, _google_email)
    except Exception:
        logger.debug("Failed to fetch userinfo during google connect for user=%s", user_id)

    # Check onboarding status
    from scheduler.db import get_user_by_id as _get_user
    db_user = _get_user(user_id)
    is_onboarded = _is_onboarded(
        user_id,
        scheduled_calendar_id=db_user.scheduled_calendar_id if db_user else None,
        scheduling_mode=db_user.scheduling_mode if db_user else "draft",
    )

    from starlette.background import BackgroundTask

    token_param = f"token={auth_token}" if auth_token else ""

    if is_onboarded:
        logger.info("google_connect: returning user=%s, renewing gmail watch", user_id)
        background = BackgroundTask(_renew_gmail_watch, user_id)
        redirect_url = f"{config.web_app_url}/settings?{token_param}"

        # If user switched to draft mode and is missing email style guide, generate it
        if db_user and db_user.scheduling_mode == "draft":
            from scheduler.db import get_guides_for_user
            guides = get_guides_for_user(user_id)
            guide_names = {g.name for g in guides}
            if "email_style" not in guide_names:
                import threading
                logger.info("google_connect: generating missing email_style guide for user=%s", user_id)
                threading.Thread(target=_run_onboarding_for_runtime, args=(user_id,), daemon=True).start()
    else:
        logger.info("google_connect: starting onboarding for user=%s", user_id)
        with _onboarding_lock:
            if _onboarding_status.get(user_id, {}).get("status") == "running":
                logger.info("google_connect: onboarding already running for user=%s", user_id)
                background = None
            else:
                from scheduler.db import update_onboarding_status
                update_onboarding_status(user_id, "pending")
                background = BackgroundTask(_run_onboarding_for_runtime, user_id)
        redirect_url = f"{config.web_app_url}/onboarding?{token_param}"

    return RedirectResponse(redirect_url, background=background)


