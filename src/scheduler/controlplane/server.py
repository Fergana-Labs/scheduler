def _strip_html(text: str) -> str:
    """Convert HTML to plain text: <br> → newline, strip tags, unescape entities."""
    text = re.sub(r'<br\s*/?>', '\n', text)
    text = re.sub(r'<[^>]+>', '', text)
    return html.unescape(text)


