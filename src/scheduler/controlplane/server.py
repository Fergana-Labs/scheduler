# --- Session store ---

sessions: dict[str, dict] = {}

_SESSION_TTL = 600  # 10 minutes — e2b sandboxes default to 5 min max


def register_session(session_token: str, user_id: str) -> None:
    """Register a session with Google credentials for a user.

    Loads credentials from the database if DATABASE_URL is set and the user
    exists. Falls back to local OAuth flow (token.json) for development.
    """
    _cleanup_expired_sessions()

    from scheduler.db import get_user_by_id

    creds = load_credentials(user_id)
    gmail = GmailClient(creds)

    db_user = get_user_by_id(user_id)
    extra_ids = (db_user.calendar_ids or []) if db_user else []
    calendar = CalendarClient(creds, config.scheduled_calendar_name, extra_calendar_ids=extra_ids)
    calendar.get_or_create_scheduled_calendar()

    sessions[session_token] = {
        "user_id": user_id,
        "gmail": gmail,
        "calendar": calendar,
        "created_at": time.time(),
    }


def _cleanup_expired_sessions() -> None:
    """Remove sessions older than _SESSION_TTL and close their API connections."""
    now = time.time()
    expired = [tok for tok, s in sessions.items() if now - s["created_at"] > _SESSION_TTL]
    for tok in expired:
        session = sessions.pop(tok)
        gmail = session.get("gmail")
        if gmail:
            gmail.close()
        cal = session.get("calendar")
        if cal:
            cal.close()


def get_session(authorization: str = Header(default="")) -> dict:
    """Validate the session token from the Authorization header.

    In self-hosted mode, returns the single user without auth.
    """
    if _is_self_hosted_mode():
        user = _get_self_hosted_user()
        if user:
            return user
        raise HTTPException(status_code=401, detail="No user configured")

    if not authorization.startswith("Bearer "):
        raise HTTPException(status_code=401, detail="Invalid authorization header")

    token = authorization.removeprefix("Bearer ")
    session = sessions.get(token)
    if not session:
        raise HTTPException(status_code=401, detail="Invalid or expired session token")

    if time.time() - session["created_at"] > _SESSION_TTL:
        del sessions[token]
        raise HTTPException(status_code=401, detail="Invalid or expired session token")

    return session


