def _sync_scheduling_link_on_send(user_id: str, email, user_timezone: str) -> None:
    """Re-analyze the sent email and update the scheduling link's windows if they changed."""
    import re
    from scheduler.db import get_scheduling_link_by_thread, update_scheduling_link_windows
    from scheduler.drafts.composer import _analyze_draft_for_scheduling

    thread_id = email.thread_id
    if not thread_id:
        return

    link = get_scheduling_link_by_thread(user_id, thread_id)
    if not link:
        return

    body = email.body or ""

    # Strip the Scheduled footer so the LLM only sees the actual email content
    footer_markers = [
        "Use Scheduled to find a time automatically",
        "sent by Scheduled",
        "Use <a href=",
        "sent by <a href=",
    ]
    for marker in footer_markers:
        idx = body.find(marker)
        if idx > 0:
            body = body[:idx].strip()
            break

    # Strip HTML tags for analysis
    clean_body = re.sub(r"<[^>]+>", "", body)
    clean_body = clean_body.replace("&amp;", "&").replace("&lt;", "<").replace("&gt;", ">").replace("&nbsp;", " ").replace("&#39;", "'").replace("&quot;", '"')

    try:
        analysis = _analyze_draft_for_scheduling(clean_body, link.attendee_email or "", user_timezone)
    except Exception:
        logger.debug("_sync_scheduling_link_on_send: failed to analyze sent body for link %s", link.id, exc_info=True)
        return

    new_windows = analysis.get("suggested_windows", [])
    if not new_windows:
        return

    # Compare with existing windows — only update if different
    import json as _json
    old_windows_str = _json.dumps(link.suggested_windows or [], sort_keys=True)
    new_windows_str = _json.dumps(new_windows, sort_keys=True)
    if old_windows_str == new_windows_str:
        return

    logger.info("_sync_scheduling_link_on_send: updating link %s windows: %s -> %s", link.id, old_windows_str, new_windows_str)
    update_scheduling_link_windows(
        link.id,
        suggested_windows=new_windows,
        duration_minutes=analysis.get("duration_minutes"),
        event_summary=analysis.get("event_summary"),
    )


def _handle_sent_message_for_invite(user_id: str, email, gmail: GmailClient, calendar: CalendarClient) -> None:
    """Check if a user-sent message should trigger a pending calendar invite."""
    from scheduler.classifier.intent import verify_sent_message_for_invite
    from scheduler.db import get_pending_invite_by_thread, delete_pending_invite, update_pending_invite

    pending = get_pending_invite_by_thread(user_id, email.thread_id)
    if not pending:
        return

    logger.info("gmail_webhook: sent message in thread %s has pending invite, verifying", email.thread_id)

    # Fetch thread context (messages before this one)
    thread_messages = []
    try:
        for t_email in gmail.get_thread(email.thread_id):
            if t_email.id == email.id:
                break
            thread_messages.append({"sender": t_email.sender, "body": t_email.body, "date": t_email.date.isoformat()})
    except Exception:
        logger.warning("gmail_webhook: failed to fetch thread for invite verification")

    result = verify_sent_message_for_invite(
        sent_message_body=email.body,
        sent_message_sender=email.sender,
        thread_messages=thread_messages,
        pending_invite=pending,
    )

    logger.info("gmail_webhook: invite verification: action=%s reason=%s", result.action, result.reason)

    if result.action == "skip":
        delete_pending_invite(pending.id)
        logger.info("gmail_webhook: deleted pending invite %s (skipped)", pending.id)
        return

    if result.action == "update":
        update_pending_invite(
            pending.id,
            attendee_emails=result.updated_attendee_emails,
            event_summary=result.updated_event_summary,
            event_start=datetime.fromisoformat(result.updated_event_start) if result.updated_event_start else None,
            event_end=datetime.fromisoformat(result.updated_event_end) if result.updated_event_end else None,
            add_google_meet=result.updated_add_google_meet,
            location=result.updated_location,
        )
        # Apply updates to pending directly to avoid a re-fetch
        if result.updated_attendee_emails:
            pending.attendee_emails = result.updated_attendee_emails
        if result.updated_event_summary:
            pending.event_summary = result.updated_event_summary
        if result.updated_event_start:
            pending.event_start = datetime.fromisoformat(result.updated_event_start)
        if result.updated_event_end:
            pending.event_end = datetime.fromisoformat(result.updated_event_end)
        if result.updated_add_google_meet is not None:
            pending.add_google_meet = result.updated_add_google_meet
        if result.updated_location is not None:
            pending.location = result.updated_location

    # action == "send" or "update" — create the calendar invite
    try:
        calendar.create_invite_event(
            summary=pending.event_summary,
            start=pending.event_start,
            end=pending.event_end,
            attendee_emails=pending.attendee_emails,
            location=pending.location,
            add_google_meet=pending.add_google_meet,
        )
        logger.info("gmail_webhook: created invite event for thread %s", email.thread_id)
    except Exception:
        logger.exception("gmail_webhook: failed to create invite event for thread %s", email.thread_id)

    delete_pending_invite(pending.id)


_REFRESH_FAILURE_THRESHOLD = 3


def _handle_refresh_error(user_id: str, email: str) -> None:
    """Increment refresh_failures; mark user for re-auth if threshold reached."""
    from scheduler.db import increment_refresh_failures, update_onboarding_status
    count = increment_refresh_failures(user_id)
    if count >= _REFRESH_FAILURE_THRESHOLD:
        logger.warning("gmail: %d consecutive RefreshErrors for user=%s, marking for re-auth", count, email)
        update_onboarding_status(user_id, "failed")
    else:
        logger.warning("gmail: RefreshError for user=%s (failure %d/%d)", email, count, _REFRESH_FAILURE_THRESHOLD)


def _get_new_message_ids(user_id: str) -> list[str]:
    """Get new Gmail message IDs since the stored history ID and advance the baseline.

    Uses the history ID returned by Gmail's History API as the new baseline,
    so no messages are lost between fetching and advancing.
    """
    from scheduler.db import get_user_by_id, update_gmail_history_id, reset_refresh_failures

    user = get_user_by_id(user_id)
    if not user or not user.gmail_history_id:
        return []

    creds = load_credentials(user_id)
    gmail = GmailClient(creds)

    try:
        new_message_ids, new_history_id = gmail.get_history(user.gmail_history_id)
    except RefreshError:
        gmail.close()
        # Retry once with fresh credentials (handles races where another
        # thread just refreshed the token).
        try:
            creds = load_credentials(user_id)
            gmail = GmailClient(creds)
            new_message_ids, new_history_id = gmail.get_history(user.gmail_history_id)
        except RefreshError:
            gmail.close()
            _handle_refresh_error(user_id, user.email)
            return []
    except Exception:
        logger.warning("gmail: history expired for user=%s, resetting", user.email)
        try:
            new_history_id = gmail.get_current_history_id()
        except RefreshError:
            gmail.close()
            _handle_refresh_error(user_id, user.email)
            return []
        update_gmail_history_id(user_id, new_history_id)
        gmail.close()
        return []

    # Success — reset failure counter
    reset_refresh_failures(user_id)
    update_gmail_history_id(user_id, new_history_id)
    gmail.close()
    return new_message_ids


def _process_messages(user_id: str, email_address: str, message_ids: list[str]) -> None:
    """Filter, classify, and draft replies for a list of Gmail message IDs."""
    from scheduler.db import get_user_by_id, try_claim_message
    from scheduler.classifier.intent import classify_email, SchedulingIntent
    from scheduler.classifier.newsletter import is_mass_email

    user = get_user_by_id(user_id)
    if not user:
        return
    if not user.system_enabled:
        logger.info("gmail: system disabled for user=%s, skipping", email_address)
        return

    logger.info("gmail: %d new message(s) for user=%s: %s", len(message_ids), email_address, message_ids)

    creds = load_credentials(user_id)
    gmail = GmailClient(creds)
    calendar = CalendarClient(creds, config.scheduled_calendar_name, extra_calendar_ids=user.calendar_ids or [])
    try:
        _process_message_batch(gmail, calendar, user, user_id, email_address, message_ids)
    finally:
        gmail.close()
        calendar.close()
        # Break reference cycles from httpx/asyncio/anyio promptly instead
        # of waiting for gen2 GC (which may not run for minutes).
        gc.collect()


def _process_message_batch(gmail, calendar, user, user_id, email_address, message_ids):
    """Inner loop extracted so _process_messages can use try/finally for cleanup."""
    for message_id in message_ids:
        if not try_claim_message(user_id, message_id):
            logger.info("gmail: message %s already claimed, skipping", message_id)
            continue

        try:
            email = gmail.get_email(message_id)

            # Skip drafts — Gmail fires messageAdded when Scheduled creates a draft
            if "DRAFT" in email.label_ids:
                logger.info("gmail: message %s is a draft, skipping", message_id)
                continue

            # User-sent messages: check if there's a pending invite for this thread
            logger.info("gmail: message %s sender=%s user=%s thread=%s subject=%s labels=%s", message_id, email.sender, email_address, email.thread_id, getattr(email, 'subject', ''), email.label_ids)
            if email.sender and email_address in email.sender:
                _handle_sent_message_for_invite(user_id, email, gmail, calendar)
                try:
                    user_tz = calendar.get_user_timezone()
                    _sync_scheduling_link_on_send(user_id, email, user_tz)
                except Exception:
                    logger.debug("gmail: failed to sync scheduling link for message %s", message_id, exc_info=True)
                try:
                    from scheduler import analytics
                    analytics.record_draft_sent(user_id, email.thread_id, email.body, email.date, message_id=message_id, sender=email.sender)
                except Exception:
                    logger.debug("analytics: failed to check sent draft for message %s", message_id, exc_info=True)
                logger.info("gmail: message %s is from the user (matched sender), skipping", message_id)
                continue

            # Skip emails from Scheduled's own sending addresses (e.g. reasoning emails)
            sender_str = email.sender or ""
            if (config.postmark_from_email and config.postmark_from_email in sender_str) or \
               (config.postmark_bot_email and config.postmark_bot_email in sender_str) or \
               "@tryscheduled.com" in sender_str or \
               "scheduled@ferganalabs.com" in sender_str:
                logger.info("gmail: message %s is from Scheduled, skipping", message_id)
                continue

            # Skip newsletters / mass emails (before classifier, saves API cost)
            if is_mass_email(email.headers, email.sender):
                logger.info("gmail: message %s is a mass email/newsletter, skipping", message_id)
                continue

            # Fetch full thread for classifier context
            thread_messages = []
            try:
                thread_emails = gmail.get_thread(email.thread_id)
                for t_email in thread_emails:
                    if "DRAFT" in t_email.label_ids:
                        continue  # skip unsent drafts
                    thread_messages.append({
                        "sender": t_email.sender,
                        "subject": t_email.subject,
                        "body": _strip_html(t_email.body) if t_email.body else "",
                        "date": t_email.date.isoformat(),
                    })
                    if t_email.id == email.id:
                        break  # include the triggering email, then stop
            except Exception:
                logger.warning("gmail: failed to fetch thread %s for context, classifying without", email.thread_id)

            classification = classify_email(
                email.subject, email.body, email.sender,
                thread_messages=thread_messages,
                recipient=email.recipient, cc=email.cc,
                user_email=email_address,
            )

            from scheduler import analytics
            analytics.track(user_id, "email_classified", {
                "intent": classification.intent.value,
                "confidence": classification.confidence,
                "is_sales_email": classification.is_sales_email,
                "message_id": message_id,
            })

            logger.info("gmail: message %s classified as %s confidence=%.2f summary=%r", message_id, classification.intent.value, classification.confidence, classification.summary)

            if classification.intent == SchedulingIntent.DOESNT_NEED_DRAFT:
                logger.info("gmail: message %s is not scheduling-related, skipping", message_id)
                continue

            # Skip cold outreach unless user opted in
            if classification.is_sales_email and not user.process_sales_emails:
                logger.info("gmail: message %s is cold outreach, skipping", message_id)
                continue

            logger.info(
                "gmail: message %s classified as %s (confidence=%.2f), composing draft",
                message_id,
                classification.intent.value,
                classification.confidence,
            )

            compose_result = _compose_draft_for_runtime(
                user_id=user_id,
                email=email,
                classification=classification,
                gmail=gmail,
                calendar=calendar,
                autopilot=user.autopilot_enabled,
                user_email=email_address,
                thread_messages=thread_messages,
            )

            compose_result = compose_result or {}
            draft_id = compose_result.get("draft_id")
            invite_proposal = compose_result.get("invite_proposal")

            if draft_id is None:
                logger.info("gmail: thread for message %s already resolved, no draft created", message_id)
            else:
                logger.info("gmail: created draft %s for message %s", draft_id, message_id)

                # Store pending invite if the composer proposed one
                if invite_proposal:
                    from scheduler.db import create_pending_invite
                    try:
                        create_pending_invite(
                            user_id=user_id,
                            thread_id=email.thread_id,
                            attendee_emails=invite_proposal["attendee_emails"],
                            event_summary=invite_proposal["event_summary"],
                            event_start=datetime.fromisoformat(invite_proposal["event_start"]),
                            event_end=datetime.fromisoformat(invite_proposal["event_end"]),
                            add_google_meet=invite_proposal.get("add_google_meet", False),
                            location=invite_proposal.get("location", ""),
                        )
                        logger.info("gmail: created pending invite for thread %s", email.thread_id)
                    except Exception:
                        logger.exception("gmail: failed to create pending invite for thread %s", email.thread_id)

                analytics.track(user_id, "draft_composed", {
                    "thread_id": email.thread_id,
                    "draft_id": draft_id,
                    "was_autopilot": draft_id.startswith("sent:") if isinstance(draft_id, str) else False,
                })
                if user.reasoning_emails_enabled:
                    try:
                        from scheduler.lifecycle.reasoning import send_reasoning_email
                        send_reasoning_email(
                            user_email=email_address,
                            thread_id=email.thread_id,
                            subject=email.subject,
                            classification=classification,
                            calendar=calendar,
                            gmail=gmail,
                            invite_proposal=invite_proposal,
                        )
                    except Exception:
                        logger.exception("gmail: failed to send reasoning email for message %s", message_id)

        except Exception as exc:
            if _is_gmail_404(exc):
                logger.warning("gmail: message %s not found (deleted?), skipping", message_id)
            else:
                logger.exception("gmail: failed to process message %s for user=%s", message_id, email_address)


@app.post("/webhooks/gmail")
async def gmail_webhook(request: Request, background_tasks: BackgroundTasks):
    """Receive Gmail push notifications via Google Cloud Pub/Sub.

    Google sends a POST with a Pub/Sub message whose data is a base64-encoded
    JSON object containing the user's email address and the latest historyId.

    Verified via a shared secret token passed as a query parameter. The Pub/Sub
    push subscription URL should be configured as:
        https://<host>/webhooks/gmail?token=<GMAIL_WEBHOOK_TOKEN>
    """
    if config.gmail_webhook_token:
        token = request.query_params.get("token", "")
        if not hmac.compare_digest(token, config.gmail_webhook_token):
            raise HTTPException(status_code=403, detail="Invalid webhook token")

    from scheduler.db import get_user_by_email

    body = await request.json()
    message = body.get("message", {})
    data_b64 = message.get("data", "")

    if not data_b64:
        raise HTTPException(status_code=400, detail="Missing message data")

    try:
        payload = json.loads(base64.urlsafe_b64decode(data_b64))
    except Exception:
        raise HTTPException(status_code=400, detail="Invalid message data")

    email_address = payload.get("emailAddress")
    history_id = str(payload.get("historyId", ""))

    if not email_address or not history_id:
        raise HTTPException(status_code=400, detail="Missing emailAddress or historyId")

    if _is_cached_unknown_gmail_webhook_email(email_address):
        logger.info("gmail_webhook: cached unknown email %s", email_address)
        return {"status": "ignored", "reason": "unknown user"}

    user = await asyncio.to_thread(get_user_by_email, email_address)
    if not user:
        # Fallback: try google_email column (handles Auth0 email != Gmail email)
        from scheduler.db import get_user_by_google_email
        user = await asyncio.to_thread(get_user_by_google_email, email_address)
    if not user:
        _cache_unknown_gmail_webhook_email(email_address)
        logger.warning("gmail_webhook: unknown email %s", email_address)
        # Return 200 so Pub/Sub doesn't retry for unknown users
        return {"status": "ignored", "reason": "unknown user"}

    if user.onboarding_status == "failed":
        logger.info("gmail_webhook: user %s pending re-auth, skipping", user.email)
        return {"status": "ok"}

    try:
        message_ids = await asyncio.to_thread(_get_new_message_ids, str(user.id))
    except (ValueError, RefreshError):
        logger.warning("gmail_webhook: user %s has no/invalid refresh token, skipping", user.email)
        return {"status": "ok"}
    if message_ids:
        background_tasks.add_task(_process_messages, str(user.id), email_address, message_ids)

    return {"status": "ok"}


