def _is_cached_unknown_gmail_webhook_email(email_address: str) -> bool:
    """Return True when an unknown webhook email was seen recently."""
    now = time.time()
    expires_at = _unknown_gmail_webhook_emails.get(email_address)
    if expires_at is None:
        return False
    if expires_at <= now:
        del _unknown_gmail_webhook_emails[email_address]
        return False
    return True


def _cache_unknown_gmail_webhook_email(email_address: str) -> None:
    """Avoid repeated DB lookups for unknown webhook emails."""
    _unknown_gmail_webhook_emails[email_address] = time.time() + _UNKNOWN_GMAIL_WEBHOOK_TTL


def _is_gmail_404(exc: Exception) -> bool:
    """Return True if exc is a Gmail API 404 (message deleted/not found)."""
    from googleapiclient.errors import HttpError

    return isinstance(exc, HttpError) and exc.resp.status == 404


def _cleanup_stale_drafts() -> int:
    """Delete Gmail drafts older than 48 hours that we created and the user never sent."""
    from itertools import groupby
    from operator import itemgetter
    from scheduler.auth.google_auth import load_credentials
    from scheduler.db import get_stale_unsent_drafts, mark_draft_auto_deleted

    stale = get_stale_unsent_drafts(hours=48)
    deleted = 0

    # Group by user so we create one GmailClient per user, not per draft
    stale_sorted = sorted(stale, key=itemgetter("user_id"))
    for user_id, drafts in groupby(stale_sorted, key=itemgetter("user_id")):
        try:
            creds = load_credentials(user_id)
        except Exception:
            logger.warning("auto_delete_draft: creds failed for user=%s, skipping", user_id, exc_info=True)
            for row in drafts:
                mark_draft_auto_deleted(row["id"])
                deleted += 1
            continue

        with GmailClient(creds) as gmail:
            for row in drafts:
                try:
                    gmail.delete_draft(row["draft_id"])
                except Exception:
                    logger.warning(
                        "auto_delete_draft: failed to delete draft_id=%s for user=%s",
                        row["draft_id"], user_id, exc_info=True,
                    )
                # Remove the DB row regardless
                mark_draft_auto_deleted(row["id"])
                deleted += 1

    return deleted


def _is_morning_window(user_tz_str: str) -> bool:
    """Return True if it's currently 6:00–9:00 AM in the user's timezone."""
    from zoneinfo import ZoneInfo

    try:
        tz = ZoneInfo(user_tz_str)
    except Exception:
        return False
    local_now = datetime.now(timezone.utc).astimezone(tz)
    return 6 <= local_now.hour < 9


def _draft_body_matches(raw_body: str | None, gmail_body: str) -> bool:
    """Check if a Gmail draft body still matches what we composed (user hasn't edited)."""
    if not raw_body:
        return True  # no raw_body stored — can't compare, assume unchanged
    # Normalize whitespace for comparison (Gmail may reformat)
    original_norm = " ".join(raw_body.split())
    gmail_norm = " ".join(gmail_body.split())
    # Strip HTML tags from Gmail body (our raw_body is plain text, Gmail may wrap in HTML)
    gmail_plain = re.sub(r'<br\s*/?>', '\n', gmail_norm)
    gmail_plain = re.sub(r'<[^>]+>', '', gmail_plain)
    gmail_plain = " ".join(gmail_plain.split())
    import difflib
    return difflib.SequenceMatcher(None, original_norm, gmail_plain).ratio() >= 0.8


def _draft_needs_refresh_today(windows: list[dict], user_tz_str: str) -> bool:
    """Return True if any suggested time is today or earlier.

    If a draft proposes 2pm Thursday, it should be refreshed by 9am Thursday —
    the user shouldn't wake up and see a draft proposing a time later that day.
    """
    from zoneinfo import ZoneInfo

    if not windows:
        return False  # no windows = availability mode, handled separately

    try:
        tz = ZoneInfo(user_tz_str)
    except Exception:
        tz = ZoneInfo("UTC")

    today = datetime.now(timezone.utc).astimezone(tz).date()

    for w in windows:
        try:
            if date.fromisoformat(w["date"]) <= today:
                return True
        except (KeyError, ValueError):
            continue

    return False


def _refresh_stale_drafts() -> int:
    """Delete stale Gmail drafts and recompose them with fresh availability."""
    from itertools import groupby
    from operator import itemgetter
    from scheduler.classifier.intent import classify_email, SchedulingIntent
    from scheduler.db import get_drafts_eligible_for_refresh, get_user_by_id, mark_draft_auto_deleted

    candidates = get_drafts_eligible_for_refresh()
    refreshed = 0

    # Group by user so we create one GmailClient + CalendarClient per user
    candidates_sorted = sorted(candidates, key=itemgetter("user_id"))
    for user_id_raw, user_drafts in groupby(candidates_sorted, key=itemgetter("user_id")):
        user_id = str(user_id_raw)
        user_drafts = list(user_drafts)  # consume the groupby iterator

        try:
            creds = load_credentials(user_id)
        except Exception:
            logger.warning("draft_refresh: creds failed for user=%s, skipping", user_id, exc_info=True)
            continue

        user = get_user_by_id(user_id)
        extra_ids = (user.calendar_ids or []) if user else []

        # CalendarClient is lazy — timezone check won't trigger GmailClient build
        with CalendarClient(creds, config.scheduled_calendar_name, extra_calendar_ids=extra_ids) as calendar:
            user_tz = calendar.get_user_timezone()
            if not _is_morning_window(user_tz):
                continue  # not morning for this user — skip all their drafts, no Gmail build

            with GmailClient(creds) as gmail:
                for row in user_drafts:
                    draft_id = row["draft_id"]
                    thread_id = row["thread_id"]
                    user_email = row["user_email"]

                    # Check if any suggested time is today or earlier
                    composed_at = row.get("composed_at")
                    windows = row.get("suggested_windows") or []
                    if isinstance(windows, str):
                        windows = json.loads(windows)
                    if windows:
                        if not _draft_needs_refresh_today(windows, user_tz):
                            continue
                    else:
                        if composed_at and (datetime.now(timezone.utc) - composed_at.replace(tzinfo=timezone.utc)) < timedelta(hours=24):
                            continue

                    # Check if user acted on the draft (edited or deleted)
                    try:
                        gmail_draft = gmail.get_draft(draft_id)
                    except Exception:
                        logger.warning("draft_refresh: failed to fetch draft %s, skipping", draft_id, exc_info=True)
                        continue

                    if gmail_draft is None:
                        logger.info("draft_refresh: draft %s deleted by user, cleaning up", draft_id)
                        mark_draft_auto_deleted(row["id"])
                        continue

                    if not _draft_body_matches(row.get("raw_body"), gmail_draft["body"]):
                        logger.info("draft_refresh: draft %s edited by user, skipping refresh", draft_id)
                        continue

                    # Delete old Gmail draft
                    try:
                        gmail.delete_draft(draft_id)
                    except Exception:
                        logger.warning("draft_refresh: failed to delete old draft %s", draft_id, exc_info=True)

                    # Re-fetch the email thread
                    try:
                        thread_emails = gmail.get_thread(thread_id)
                    except Exception:
                        logger.warning("draft_refresh: thread %s gone, cleaning up draft", thread_id, exc_info=True)
                        mark_draft_auto_deleted(row["id"])
                        continue

                    if not thread_emails:
                        mark_draft_auto_deleted(row["id"])
                        continue

                    user_replied = any(
                        t_email.sender and user_email in t_email.sender and composed_at and t_email.date > composed_at
                        for t_email in thread_emails
                    )
                    if user_replied:
                        logger.info("draft_refresh: thread %s resolved (user replied), cleaning up", thread_id)
                        mark_draft_auto_deleted(row["id"])
                        continue

                    email = thread_emails[-1]
                    thread_messages = []
                    for t_email in thread_emails:
                        if "DRAFT" in t_email.label_ids:
                            continue
                        thread_messages.append({
                            "sender": t_email.sender,
                            "subject": t_email.subject,
                            "body": _strip_html(t_email.body) if t_email.body else "",
                            "date": t_email.date.isoformat(),
                        })

                    classification = classify_email(
                        email.subject, email.body, email.sender,
                        thread_messages=thread_messages,
                        recipient=email.recipient, cc=email.cc,
                        user_email=user_email,
                    )

                    if classification.intent == SchedulingIntent.DOESNT_NEED_DRAFT:
                        logger.info("draft_refresh: thread %s no longer needs draft, cleaning up", thread_id)
                        mark_draft_auto_deleted(row["id"])
                        continue

                    mark_draft_auto_deleted(row["id"])

                    try:
                        compose_result = _compose_draft_for_runtime(
                            user_id=user_id,
                            email=email,
                            classification=classification,
                            gmail=gmail,
                            calendar=calendar,
                            autopilot=user.autopilot_enabled if user else False,
                            user_email=user_email,
                            thread_messages=thread_messages,
                            refresh_count=row["refresh_count"] + 1,
                        )
                        if compose_result and compose_result.get("draft_id"):
                            logger.info("draft_refresh: refreshed draft for thread %s (refresh #%d)", thread_id, row["refresh_count"] + 1)
                            refreshed += 1
                        else:
                            logger.info("draft_refresh: thread %s resolved during recomposition, no new draft", thread_id)
                    except Exception:
                        logger.exception("draft_refresh: failed to recompose draft for thread %s", thread_id)

    return refreshed


_DRAFT_REFRESH_INTERVAL = 1800  # 30 minutes


def _log_memory_snapshot():
    """Log top tracemalloc allocations — temporary, for diagnosing the memory leak."""
    if not tracemalloc.is_tracing():
        return
    snapshot = tracemalloc.take_snapshot()
    snapshot = snapshot.filter_traces([
        tracemalloc.Filter(False, "<frozen importlib._bootstrap>"),
        tracemalloc.Filter(False, "<frozen importlib._bootstrap_external>"),
    ])
    current, peak = tracemalloc.get_traced_memory()
    logger.info("MEMPROFILE current=%.1fMB peak=%.1fMB", current / 1024 / 1024, peak / 1024 / 1024)
    for stat in snapshot.statistics("filename")[:15]:
        logger.info("MEMPROFILE  %s: %.1fMB (%d allocs)", stat.traceback, stat.size / 1024 / 1024, stat.count)
    for stat in snapshot.statistics("lineno")[:10]:
        logger.info("MEMPROFILE_LINE  %s: %.1fMB (%d allocs)", stat.traceback, stat.size / 1024 / 1024, stat.count)


async def _draft_refresh_loop():
    """Background loop: refresh stale drafts before users' mornings."""
    await asyncio.sleep(120)  # let server finish starting
    while True:
        try:
            refreshed = await asyncio.to_thread(_refresh_stale_drafts)
            if refreshed:
                logger.info("draft_refresh_loop: refreshed %d stale drafts", refreshed)
        except Exception:
            logger.exception("draft_refresh_loop: failed")
        gc.collect()
        _log_memory_snapshot()
        await asyncio.sleep(_DRAFT_REFRESH_INTERVAL)


def _cleanup_in_memory_caches() -> None:
    """Purge expired entries from all module-level dicts to prevent memory leaks."""
    now = time.time()

    # OAuth states older than 10 minutes (callback timeout is 600s)
    expired = [k for k, ts in _oauth_states.items() if now - ts > 600]
    for k in expired:
        del _oauth_states[k]

    expired = [k for k, ts in _google_connect_states.items() if now - ts > 600]
    for k in expired:
        del _google_connect_states[k]

    # Demo rate limiter: remove IPs with no recent hits
    expired = [ip for ip, hits in _demo_rate.items() if not hits or all(now - t > _DEMO_RATE_WINDOW for t in hits)]
    for ip in expired:
        del _demo_rate[ip]

    # Unknown gmail webhook cache: remove expired entries
    expired = [e for e, exp_at in _unknown_gmail_webhook_emails.items() if exp_at <= now]
    for e in expired:
        del _unknown_gmail_webhook_emails[e]

    # Onboarding status: remove failed entries older than 10 minutes
    with _onboarding_lock:
        expired = [uid for uid, s in _onboarding_status.items()
                   if s.get("status") == "failed" and now - s.get("failed_at", 0) > 600]
        for uid in expired:
            del _onboarding_status[uid]

    # Sessions
    _cleanup_expired_sessions()


async def _watch_renewal_loop():
    """Background loop: renew Gmail watches and clean up old processed messages."""
    await asyncio.sleep(60)  # let server finish starting
    while True:
        try:
            from scheduler.db import cleanup_processed_messages
            from scheduler.gmail.watch import renew_all_watches

            result = await asyncio.to_thread(renew_all_watches)
            logger.info("watch_renewal_loop: %s", result)
            deleted = await asyncio.to_thread(cleanup_processed_messages)
            if deleted:
                logger.info("watch_renewal_loop: cleaned up %d old processed_messages rows", deleted)
            from scheduler.db import cleanup_expired_scheduling_links
            expired_links = await asyncio.to_thread(cleanup_expired_scheduling_links)
            if expired_links:
                logger.info("watch_renewal_loop: expired %d scheduling links", expired_links)
            deleted_drafts = await asyncio.to_thread(_cleanup_stale_drafts)
            if deleted_drafts:
                logger.info("watch_renewal_loop: auto-deleted %d stale drafts", deleted_drafts)
            _cleanup_in_memory_caches()

            # Renew bot Gmail watch (if configured)
            try:
                await _bot_watch_renewal()
            except Exception:
                logger.debug("watch_renewal_loop: bot watch renewal failed", exc_info=True)

            # Clean up old bot processed messages
            try:
                from scheduler.db import cleanup_bot_processed_messages
                bot_cleaned = await asyncio.to_thread(cleanup_bot_processed_messages)
                if bot_cleaned:
                    logger.info("watch_renewal_loop: cleaned up %d old bot_processed_messages", bot_cleaned)
            except Exception:
                logger.debug("watch_renewal_loop: bot message cleanup failed", exc_info=True)

        except Exception:
            logger.exception("watch_renewal_loop: failed")
        await asyncio.sleep(_WATCH_RENEWAL_INTERVAL)


async def _gmail_poll_loop():
    """Background loop (self-hosted): poll Gmail for new messages via History API."""
    await asyncio.sleep(10)  # let server finish starting

    last_cleanup_at = 0.0

    while True:
        try:
            from scheduler.db import get_all_user_ids, get_user_by_id

            user_ids = await asyncio.to_thread(get_all_user_ids)
            for user_id in user_ids:
                try:
                    user = await asyncio.to_thread(get_user_by_id, user_id)
                    if not user or not user.google_refresh_token:
                        continue
                    if not user.system_enabled:
                        continue
                    if user.scheduling_mode == "bot":
                        continue
                    if not user.gmail_history_id:
                        continue

                    message_ids = await asyncio.to_thread(_get_new_message_ids, str(user.id))
                    if message_ids:
                        await asyncio.to_thread(
                            _process_messages, str(user.id), user.email, message_ids
                        )
                except Exception:
                    logger.exception("gmail_poll: failed for user=%s", user_id)
        except Exception:
            logger.exception("gmail_poll: failed to list users")

        # Periodic cleanup (every hour, not every poll cycle)
        if time.time() - last_cleanup_at >= 3600:
            last_cleanup_at = time.time()
            try:
                from scheduler.db import cleanup_processed_messages
                deleted = await asyncio.to_thread(cleanup_processed_messages)
                if deleted:
                    logger.info("gmail_poll: cleaned up %d old processed_messages rows", deleted)
            except Exception:
                logger.exception("gmail_poll: cleanup failed")
            _cleanup_in_memory_caches()

        global _last_poll_at
        _last_poll_at = time.time()

        await asyncio.sleep(config.watcher_poll_interval)


async def _bot_inbox_poll_loop():
    """Background loop: poll the bot's Gmail inbox for new CC'd messages."""
    from scheduler.bot.gmail import is_bot_mode_configured, get_bot_gmail_client

    await asyncio.sleep(15)  # let server finish starting

    if not is_bot_mode_configured():
        logger.info("bot_poll: bot mode not configured, skipping poll loop")
        return

    while True:
        try:
            from scheduler.db import get_bot_history_id, update_bot_history_id

            history_id = get_bot_history_id()
            if not history_id:
                # Initialize history ID from bot's current state
                bot_gmail = get_bot_gmail_client()
                history_id = bot_gmail.get_current_history_id()
                update_bot_history_id(history_id)
                logger.info("bot_poll: initialized history_id=%s", history_id)
                await asyncio.sleep(config.watcher_poll_interval)
                continue

            bot_gmail = get_bot_gmail_client()
            message_ids, new_history_id = bot_gmail.get_history(history_id)
            update_bot_history_id(new_history_id)

            if message_ids:
                logger.info("bot_poll: %d new message(s)", len(message_ids))
                await asyncio.to_thread(_process_bot_messages, message_ids)

        except Exception:
            logger.exception("bot_poll: failed")

        await asyncio.sleep(config.watcher_poll_interval)


async def _bot_watch_renewal():
    """Renew the bot Gmail watch (called from watch renewal loop)."""
    from scheduler.bot.gmail import is_bot_mode_configured, get_bot_gmail_client
    from scheduler.db import get_bot_history_id, update_bot_history_id, update_bot_watch_expiration

    if not is_bot_mode_configured():
        return

    bot_gmail = get_bot_gmail_client()
    topic = config.bot_gmail_pubsub_topic
    if not topic:
        return

    result = bot_gmail.watch(topic)
    update_bot_watch_expiration(int(result["expiration"]))

    # Ensure we have a history ID
    if not get_bot_history_id():
        update_bot_history_id(result["historyId"])

    logger.info("bot_watch: renewed, expires=%s", result["expiration"])


@asynccontextmanager
async def lifespan(app: FastAPI):
    # Start memory tracing so /admin/memory-profile can show top allocators
    tracemalloc.start(25)  # 25 frames deep for meaningful tracebacks

    if _is_self_hosted_mode():
        task = asyncio.create_task(_gmail_poll_loop())
    else:
        task = asyncio.create_task(_watch_renewal_loop())
    # Draft refresh only works with Postgres (composed_drafts table)
    refresh_task = None
    if not _is_self_hosted_mode():
        refresh_task = asyncio.create_task(_draft_refresh_loop())
    # Bot inbox polling (runs alongside the user-mode loops)
    bot_task = asyncio.create_task(_bot_inbox_poll_loop())
    yield
    task.cancel()
    if refresh_task:
        refresh_task.cancel()
    bot_task.cancel()
    tracemalloc.stop()


