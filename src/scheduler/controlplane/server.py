def _run_onboarding_all(user_id: str) -> None:
    """Background task: run backfill + both guide-writer agents for a user, then set up Gmail watch."""
    import anyio
    from scheduler.gmail.watch import setup_gmail_watch
    from scheduler.guides.backends import LocalGuideBackend
    from scheduler.guides.preferences import run_preferences_agent
    from scheduler.guides.style import run_style_agent
    from scheduler.onboarding.agent import _run_backfill_async as _run_backfill
    from scheduler.onboarding.backends import LocalBackend

    creds = load_credentials(user_id)
    gmail = GmailClient(creds)
    calendar = CalendarClient(creds, config.scheduled_calendar_name)
    try:
        cal_id = calendar.get_or_create_scheduled_calendar()
        if cal_id:
            from scheduler.db import update_scheduled_calendar_id
            update_scheduled_calendar_id(user_id, cal_id)

        guide_backend = LocalGuideBackend(gmail, calendar, user_id=user_id)
        onboarding_backend = LocalBackend(gmail, calendar)

        async def _run():
            async with anyio.create_task_group() as tg:
                tg.start_soon(_run_backfill, onboarding_backend, config.onboarding_lookback_days)
                tg.start_soon(run_preferences_agent, guide_backend)
                tg.start_soon(run_style_agent, guide_backend)

        anyio.run(_run)
    finally:
        gmail.close()
        calendar.close()

    # Send lifecycle welcome email (non-blocking — failure never stops onboarding)
    try:
        from scheduler.lifecycle.welcome import send_lifecycle_email

        send_lifecycle_email(user_id)
    except Exception:
        logger.exception("onboarding: lifecycle email failed for user=%s", user_id)

    # Set up Gmail watch so incoming emails are processed
    try:
        setup_gmail_watch(user_id)
        logger.info("onboarding: gmail watch set up for user=%s", user_id)
    except Exception:
        logger.exception("onboarding: failed to set up gmail watch for user=%s", user_id)


def _run_onboarding_for_runtime(user_id: str) -> None:
    """Dispatch onboarding to the configured runtime.

    Called as a background task after OAuth. The entire body is wrapped in
    try/except so failures always get logged and persisted to DB — no silent drops.
    """
    logger.info("onboarding: background task starting for user=%s", user_id)

    try:
        from scheduler.db import update_onboarding_status, update_system_enabled

        with _onboarding_lock:
            if _onboarding_status.get(user_id, {}).get("status") == "running":
                logger.info("onboarding: already running for user=%s, skipping", user_id)
                return
            _onboarding_status[user_id] = {
                "status": "running",
                "agents": {
                    "backfill": "running",
                    "preferences": "running",
                    "style": "running",
                },
            }

        update_onboarding_status(user_id, "running")

        runtime = config.agent_runtime.strip().lower()
        if runtime == "local":
            _run_onboarding_all(user_id)
            update_system_enabled(user_id, True)
            update_onboarding_status(user_id, "done")
            from scheduler import analytics
            analytics.track(user_id, "onboarding_completed", {})
            logger.info("onboarding: system enabled for user=%s", user_id)
            with _onboarding_lock:
                _onboarding_status.pop(user_id, None)
            return

        if runtime == "e2b":
            control_plane_url = config.control_plane_public_url.strip()
            if not control_plane_url:
                raise RuntimeError("CONTROL_PLANE_PUBLIC_URL must be set when AGENT_RUNTIME=e2b")

            # Ensure Scheduled calendar exists before sandbox launch (agents need it)
            creds = load_credentials(user_id)
            with CalendarClient(creds, config.scheduled_calendar_name) as calendar:
                cal_id = calendar.get_or_create_scheduled_calendar()
            if cal_id:
                from scheduler.db import update_scheduled_calendar_id
                update_scheduled_calendar_id(user_id, cal_id)

            def _on_agent_done(agent_name: str, success: bool) -> None:
                with _onboarding_lock:
                    entry = _onboarding_status.get(user_id)
                    if entry and "agents" in entry:
                        entry["agents"][agent_name] = "done" if success else "failed"

            from scheduler.run_e2b import launch_onboarding_in_sandbox

            launch_onboarding_in_sandbox(
                user_id=user_id,
                control_plane_url=control_plane_url,
                lookback_days=config.onboarding_lookback_days,
                on_agent_done=_on_agent_done,
            )

            # Send lifecycle welcome email (runs on control plane, not in sandbox)
            try:
                from scheduler.lifecycle.welcome import send_lifecycle_email

                send_lifecycle_email(user_id)
            except Exception:
                logger.exception("onboarding[e2b]: lifecycle email failed for user=%s", user_id)

            # Set up Gmail watch so incoming emails are processed
            from scheduler.gmail.watch import setup_gmail_watch

            try:
                setup_gmail_watch(user_id)
                logger.info("onboarding[e2b]: gmail watch set up for user=%s", user_id)
            except Exception:
                logger.exception("onboarding[e2b]: failed to set up gmail watch for user=%s", user_id)

            update_system_enabled(user_id, True)
            update_onboarding_status(user_id, "done")
            from scheduler import analytics
            analytics.track(user_id, "onboarding_completed", {})
            logger.info("onboarding[e2b]: system enabled for user=%s", user_id)

            with _onboarding_lock:
                _onboarding_status.pop(user_id, None)
            return

        raise RuntimeError(f"Unsupported AGENT_RUNTIME: {config.agent_runtime}")
    except Exception as e:
        logger.exception("onboarding: FAILED for user=%s", user_id)
        try:
            update_onboarding_status(user_id, "failed")
        except Exception:
            logger.exception("onboarding: could not even set failed status in DB for user=%s", user_id)
        with _onboarding_lock:
            _onboarding_status[user_id] = {"status": "failed", "error": str(e), "failed_at": time.time()}


def _compose_draft_for_runtime(
    user_id: str,
    email,
    classification,
    gmail: GmailClient,
    calendar: CalendarClient,
    autopilot: bool,
    user_email: str = "",
    thread_messages: list[dict] | None = None,
    refresh_count: int = 0,
) -> dict | None:
    runtime = config.agent_runtime.strip().lower()

    if runtime == "local":
        from scheduler.drafts.composer import DraftComposer, LocalDraftBackend

        backend = LocalDraftBackend(gmail, calendar, user_id=user_id, thread_messages=thread_messages, refresh_count=refresh_count)
        composer = DraftComposer(backend, user_id, autopilot=autopilot, user_email=user_email)
        return composer.compose_and_create_draft(email, classification)

    if runtime == "e2b":
        control_plane_url = config.control_plane_public_url.strip()
        if not control_plane_url:
            raise RuntimeError("CONTROL_PLANE_PUBLIC_URL must be set when AGENT_RUNTIME=e2b")

        from scheduler.run_e2b import launch_draft_composer_in_sandbox

        return launch_draft_composer_in_sandbox(
            user_id=user_id,
            control_plane_url=control_plane_url,
            email_payload=_serialize_email(email),
            classification_payload={
                "intent": classification.intent.value,
                "confidence": classification.confidence,
                "summary": classification.summary,
                "proposed_times": classification.proposed_times,
                "participants": classification.participants,
                "duration_minutes": classification.duration_minutes,
            },
            autopilot=autopilot,
            user_email=user_email,
        )

    raise RuntimeError(f"Unsupported AGENT_RUNTIME: {config.agent_runtime}")


